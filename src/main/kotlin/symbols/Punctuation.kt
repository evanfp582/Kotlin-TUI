package symbols
//Source: https://github.com/ehmicky/cross-platform-terminal-characters?tab=readme-ov-file#punctuation

object Punctuation {
    const val INVERTED_EXCLAMATION: Char = '\u00a1'  // ⁿ
    const val DOUBLE_EXCLAMATION: Char = '\u203c'  // ⁿ
    const val INVERTED_QUESTION_MARK: Char = '\u00bf'  // ⁿ

    const val ELLIPSIS: Char = '\u2026'  // …

    const val DOUBLE_LEFT_ARROW: Char = '\u00ab'  // «
    const val DOUBLE_RIGHT_ARROW: Char = '\u00bb'  // »
    const val LEFT_ARROW: Char = '\u2039'  // ‹
    const val RIGHT_ARROW: Char = '\u203a'  // ›

    const val SECTION_MARK: Char = '\u00a7'  // §
    const val PARAGRAPH_MARK: Char = '\u00b6'  // ¶
}