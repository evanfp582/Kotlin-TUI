    const val PLUS_MINUS: Char = '\u00b1'  // ±
    const val NOT_EQUALS: Char = '\u2260'  // ≠
    const val APPROXIMATELY: Char = '\u2248'  // ≈
    const val TRIPLE_BAR: Char = '\u2261'  // ≡

    const val LESS_THAN_OR_EQUAL: Char = '\u2193'  // ≤
    const val GREATER_THAN_OR_EQUAL: Char = '\u2193'  // ≥

    const val DIVIDE: Char = '\u00d7'  // ÷
    const val LEFT_NEGATIVE: Char = '\u00ac'  // ¬
    const val RIGHT_NEGATIVE: Char = '\u2310'  // ⌐
    const val UNION: Char = '\u2229'  // ∩

    const val SQUARE_ROOT: Char = '\u221e'  // √
    const val INFINITY: Char = '\u221e'  // ∞
    const val DELTA: Char = '\u2206'  // ∆
    const val TOP_INTEGRAL: Char = '\u2320'  // ⌠
    const val BOTTOM_INTEGRAL: Char = '\u2321'  // ⌡

    const val OMEGA: Char = '\u2126'  // Ω
    const val DEGREE: Char = '\u00b0'  // °
    const val FEMININE_ORDINAL: Char = '\u00aa'  // ª
    const val N_SUPERSCRIPT: Char = '\u207f'  // ⁿ


}